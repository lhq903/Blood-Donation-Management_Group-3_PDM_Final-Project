    @SuppressWarnings("empty-statement")
    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(txtPhone.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter phone number to search.");
            return;
        }
        try {
            Donor dor = new Donor();
            
            Donor_admin dn = dor.findByPhone(txtPhone.getText());
            
            if (dn != null){
                txtAddr.setText(dn.getAddress());
                // Age
                int txtage = dn.getAge();
                String age = String.valueOf(txtage);
                txtAge.setText(age);
                
                txtBloodtype.setText(dn.getblood_type());
                txtEmail.setText(dn.getEmail());
                txtMedical.setText(dn.getmed_cond());
                txtName.setText(dn.getName());
                txtPass.setText(dn.getPassword());
                rdbMale.setSelected(dn.getGender()==1);
                rdbFemale.setSelected(dn.getGender()==0);
                txtId.setText(dn.getDid());
                Date txtdb = dn.getdob();
                String birthday = String.valueOf(txtdb);
                txtdob.setText(birthday);
            } else{
                JOptionPane.showMessageDialog(this, "Blood donors don't exist");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

