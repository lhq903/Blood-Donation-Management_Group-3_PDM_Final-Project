    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtName.getText().equals("")){
            sb.append("Error");
            txtName.setBackground(Color.red);
        } else {
            txtName.setBackground(Color.white);
        }
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb);
            return;
        }
        
        try {
            Donor_admin don = new Donor_admin();
            don.setName(txtName.getText());
            don.setAddress(txtAddr.getText());
            
            // set Age
            String txtage = txtAge.getText();
            int age = Integer.parseInt(txtage);
            don.setAge(age);
            
            don.setblood_type(txtBloodtype.getText());
            don.setEmail(txtEmail.getText());
            don.setmed_cond(txtMedical.getText());
            don.setPassword(txtPass.getText());
            don.setPhone(txtPhone.getText());
            don.setGender(rdbMale.isSelected()? 1: 0);
            
            //set age
            String txtdb = txtdob.getText();
            Date dob = Date.valueOf(txtdb);
            don.setdob(dob);
            
            Donor dn = new Donor();
            dn.insert(don);
            
            JOptionPane.showMessageDialog(this, "Succesfully!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

